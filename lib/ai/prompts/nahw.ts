export function injectNahwRules(targetTopics?: string[]): string {
  let text = '\n## قواعد نحوية مرجعية (راجعها عند كل إعراب):\n\n';

  if (targetTopics && targetTopics.length > 0) {
    const allRules = Object.values(NAHW_RULES);
    for (const topic of targetTopics) {
      const found = allRules.find(
        (r) => r.title.includes(topic) || r.definition.includes(topic)
      );
      if (found) {
        text += `### ${found.title}\n`;
        text += `${found.definition}\n`;
        if ('types' in found) {
          for (const t of (found as { types: Array<{ name: string; rule?: string; example: string }> }).types) {
            text += `- ${t.name}: ${t.example}`;
            if (t.rule) text += ` (${t.rule})`;
            text += '\n';
          }
        }
        if ('examples' in found) {
          for (const ex of (found as { examples: string[] }).examples) {
            text += `- مثال: ${ex}\n`;
          }
        }
        text += '\n';
      }
    }
  } else {
    for (const [, rules] of Object.entries(NAHW_RULES)) {
      text += `### ${rules.title}\n`;
      text += `${rules.definition}\n\n`;
    }
  }

  return text;
}